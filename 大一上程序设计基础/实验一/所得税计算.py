#用户输入的应征税收入计算所得税应征税收入
#所得税起始 截止0 50000 0 以上的 5%50000 100000 2500+50000 以上的 7%100000 …… 6000+100000 以上的 
a=int(input("请输入应征税的收入："))
if(0<a<=50000):
    print("所得税为:",a*0.05)
elif (50000<a<=100000):
    print("所得税为:",0.07*(a-50000)+2500)
else:
    print("所得税为:",0.09*(a-100000)+6000)
